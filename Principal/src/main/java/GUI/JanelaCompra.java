    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Produto produto = Estoque.ObterEstoque().RetornarProdutoDeID(produtoSelecionado);
        if(produto.INT_ValorOrdenavel(AtributosProduto.AVAILABLE_AMOUNT) > 0)
        {
            Estoque.ObterEstoque().ComprarProduto(produto);
            JOptionPane.showMessageDialog(null, "Produto adquirido com sucesso.");
        }
        else
        {
            JOptionPane.showMessageDialog(null, "NÃ£o temos mais este produto em estoque.");
        }
        //Initialize(Estoque.ObterEstoque().ObterArrayProduto());
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        if(jComboBox1.getSelectedItem() != null)
        {
            String[] arrayItemLista = jComboBox1.getSelectedItem().toString().split("-");
            produtoSelecionado = Integer.parseInt(arrayItemLista[0]);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    public void Initialize(Produto[] produtos)
    {
        jComboBox1.removeAllItems();
        for(Produto produto : produtos)
        {            
            jComboBox1.addItem(produto.INT_ValorOrdenavel(AtributosProduto.ID) + "- " + produto.STRING_ValorOrdenavel(AtributosProduto.NAME));
        }
    }
